package com.pvthach.capstone.restaurant.dto.response;



import com.pvthach.capstone.restaurant.dto.DishDTO;

import java.io.Serializable;
import java.util.List;

/**
 * Created by THACH-PC
 */

public class DetailDishDTO implements Serializable {

    private DishDTO dto;

    private List<DishDTO> relatedDishes;

    public DetailDishDTO() {
    }

    public DishDTO getDto() {
        return dto;
    }

    public void setDto(DishDTO dto) {
        this.dto = dto;
    }

    public List<DishDTO> getRelatedDishes() {
        return relatedDishes;
    }

    public void setRelatedDishes(List<DishDTO> relatedDishes) {
        this.relatedDishes = relatedDishes;
    }
}